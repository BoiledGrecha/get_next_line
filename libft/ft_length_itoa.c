#include "libft.h"

int		ft_length_itoa(int n)
{
	int		len;

	len = 0;
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	while (n >= 10)
	{
		len++;
		n = n / 10;
	}
	return (++len);
}
